class MyHomePage extends StatefulWidget {
  MyHomePage({Key? key}) : super(key: key);

  @override
  _MyHomePageState createState() => _MyHomePageState();
}

class _MyHomePageState extends State<MyHomePage> {
  SpeechToText _speechToText = SpeechToText();
  bool _speechEnabled = false;
  String _lastWords = '';
  String text = 'Very fine and you?';

  @override
  void initState() {
    super.initState();
    _initSpeech();
    _loadMessages();
  }

  /// This has to happen only once per app
  void _initSpeech() async {
    _speechEnabled = await _speechToText.initialize();
    setState(() {});
  }

  /// Each time to start a speech recognition session
  void _startListening() async {
    print("LISTEN...");
    await _speechToText.listen(onResult: _onSpeechResult);
    setState(() {});
  }

  /// Manually stop the active speech recognition session
  /// Note that there are also timeouts that each platform enforces
  /// and the SpeechToText plugin supports setting timeouts on the
  /// listen method.
  void _stopListening() async {
    await _speechToText.stop();
    setState(() {});
  }

  /// This is the callback that the SpeechToText plugin calls when
  /// the platform returns recognized words.
  void _onSpeechResult(SpeechRecognitionResult result) {
    setState(() {
      _lastWords = result.recognizedWords;
      print("Palabras?: " + _lastWords);

      if (_speechToText.isNotListening) {
        if (_lastWords.toUpperCase() == "Very fine and you".toUpperCase()) {
          final textMessage = types.TextMessage(
            author: _user,
            createdAt: DateTime.now().millisecondsSinceEpoch,
            id: const Uuid().v4(),
            text: "Very fine and you?",
          );

          _addMessage(textMessage);

          final textMessage2 = types.TextMessage(
            author: _user2,
            createdAt: DateTime.now().millisecondsSinceEpoch,
            id: const Uuid().v4(),
            text: "Good. What is you do today?",
          );
          _addMessage(textMessage2);
          setState(() {
            text = "I'm searching new movies";
          });
        } else if (_lastWords.toUpperCase() ==
            "I'm searching new movies".toUpperCase()) {
          final textMessage = types.TextMessage(
            author: _user,
            createdAt: DateTime.now().millisecondsSinceEpoch,
            id: const Uuid().v4(),
            text: "I'm searching new movies",
          );
          _addMessage(textMessage);
          setState(() {
            text = "[Finalized]";
          });
        } else {
          showDialog(
            context: context,
            builder: (BuildContext context) {
              return AlertDialog(
                title: Text("Try again"),
                content: Text(_lastWords),
              );
            },
          );
        }
      }
    });
  }

